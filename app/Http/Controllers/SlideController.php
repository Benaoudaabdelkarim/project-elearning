        if($request->hasFile('image')){
            $name = time() . '.' . $request->image->getClientOriginalExtension();
            $request->image->move(public_path('images'),$name);
        }

        $url = explode('/',url()->current());

        Slide::create([
            'title' => $request->title,
            'body' => $request->body,
            'photo' => '/images/' . $name,
        ]);
        return response()->json(['message' => 'Slide added']);
    }

